	var lis = document.getElementById("faucets").getElementsByTagName("li");
	var current = document.getElementById("current");

	var defaultValue = 0;
	var minValue = 0;
	var maxValue = s.length;

	var counter = Math.max(Math.min(defaultValue, maxValue), minValue);

	function previous() {
		counter -= 1;
		counter = Math.max(Math.min(counter, maxValue), minValue);
		update();
	}
	
	function next() {
		counter += 1;
		counter = Math.max(Math.min(counter, maxValue), minValue);
		update();
	}
	
	function jump(x) {
		document.getElementById("myframe").src=s[x]
		counter = x;
		
		// Set the current label
		current.innerHTML = "Faucet " + x + " of " + (s.length -1);
	}
	
	function update() {
		document.getElementById("myframe").src=s[counter];
		
		// Highlight li links using the current counter value
		$('#faucets').children().removeClass('active');
		lis[counter].className="active";
		
		// Set the current label
		current.innerHTML = "Faucet " + counter + " of " + (s.length -1);
	}
	
	update();
	
